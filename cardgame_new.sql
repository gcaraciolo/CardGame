/*
 create tables
*/

CREATE TABLE IF NOT EXISTS "answers" (
  	"id" integer NOT NULL,
	"text" text NOT NULL,
  	PRIMARY KEY ("id")
);

CREATE TABLE IF NOT EXISTS "subjects" (
	"id" integer NOT NULL ,
	"name" varchar(255) DEFAULT NULL,
	PRIMARY KEY ("id")
);

CREATE TABLE IF NOT EXISTS "questions" (
	"id" integer NOT NULL ,
	"text" text DEFAULT NULL,
	"subject_id" integer references subjects("id"),
	"answer1_id" integer references answers("id"),
	"answer2_id" integer references answers("id"),
	"answer3_id" integer references answers("id"),
	"answer4_id" integer references answers("id"),
	"correct_answer_id" integer references answers("id"),
	PRIMARY KEY ("id")
);

CREATE TABLE IF NOT EXISTS "cards" (
  	"id" integer NOT NULL ,
  	"question_id" integer references questions("id"),
	"attack" integer DEFAULT NULL,
	"defense" integer DEFAULT NULL,
	"life" integer DEFAULT NULL,
	PRIMARY KEY ("id")
);

CREATE TABLE IF NOT EXISTS "decks" (
	"id" integer NOT NULL,
  	PRIMARY KEY ("id")
);

CREATE TABLE IF NOT EXISTS "chars" (
	"id" integer NOT NULL ,
	"deck_id" integer references decks("id"),
	"name" varchar(50) NOT NULL,
	PRIMARY KEY ("id")
);

CREATE TABLE IF NOT EXISTS "users" (
	"id" integer NOT NULL,
	"char_id" integer references chars("id"),
	"username" varchar(255) DEFAULT NULL,
	"password" varchar(50) DEFAULT NULL,
	"wins_count" integer DEFAULT NULL,
	"lost_count" integer DEFAULT NULL,
	PRIMARY KEY ("id")
);

CREATE TABLE IF NOT EXISTS "decks_cards" (
	"id" integer NOT NULL ,
  	"deck_id" integer references decks("id"),
  	"card_id" integer references cards("id"),
  	PRIMARY KEY ("id")
);

